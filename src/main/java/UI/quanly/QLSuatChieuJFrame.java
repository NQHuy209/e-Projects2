    private void showDataTable() {
        tableModel.setRowCount(0);
        for (ScheduleModel sModel : ScheduleDAO.sList) {
            tableModel.addRow(new Object[] {
                sModel.getId_schedule(),
                sModel.getDay(),
                sModel.getShowtime()
            });
        }
    }
    

    private void showDataTable() {
        tableModel.setRowCount(0);
        for (RoomSeatModel sModel : RoomSeatDAO.rSeatList) {
            tableModel.addRow(new Object[] {
                sModel.getId_room_seat(),
                sModel.getSeat_column(),
                sModel.getSeat_row(),
                sModel.getType(),
                sModel.getPrice()
            });
        }
    }
    
